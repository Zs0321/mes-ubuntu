#!/bin/bash
# 启用权限管理系统的快速脚本

echo "=========================================="
echo "启用权限管理系统"
echo "=========================================="

# 检查Python环境
if ! command -v python3 &> /dev/null; then
    echo "错误: 未找到 python3"
    exit 1
fi

# 运行初始化脚本
echo ""
echo "步骤 1: 初始化权限数据库"
echo "------------------------------------------"

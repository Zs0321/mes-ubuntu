echo "步骤 3: 登录系统"
echo "------------------------------------------"
echo "使用您的群晖NAS账号登录系统"
echo "管理员账户将自动获得完整权限"
echo ""
echo "=========================================="
echo "✓ 权限系统启用完成"
echo "=========================================="

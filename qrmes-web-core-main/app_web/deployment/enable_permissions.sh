if [ $? -ne 0 ]; then
    echo ""
    echo "✗ 数据库初始化失败"
    exit 1
fi

echo ""
echo "步骤 2: 重启服务"
echo "------------------------------------------"
echo "请手动重启 mesapp.py 服务:"
echo "  1. 按 Ctrl+C 停止当前服务"
